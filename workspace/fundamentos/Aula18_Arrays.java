package br.com.fundamentos;

public class Aula18_Arrays {

	public static void main(String[] args) {
		// Arrays Java - As matrizes são usadas para armazenar vários valores em uma única variável, 
		// em vez de declarar variáveis ​​separadas para cada valor.
		
		String[] frutas = {"Uva","Maça","Banana","Laranja","Goiaba"};
		
		for (String i : frutas) {
			System.out.println(i);
		}

	}

}
