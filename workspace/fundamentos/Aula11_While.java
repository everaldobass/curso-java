package br.com.fundamentos;

public class Aula11_While {

	public static void main(String[] args) {
		// O Loop While
		
		int i = 0;
		while (i <= 5) {
			 System.out.println(" Valor de X: " + i);
			
			i++;
		}
		
		System.out.println("========== Do While ============");
		
		
		// O Loop Do / While
		 int x = 0;
		 
		 do {
			 System.out.println(" Valor de X: " + x);
			 
			 x++;
			 
		 } while (x < 5);
		

	}

}
