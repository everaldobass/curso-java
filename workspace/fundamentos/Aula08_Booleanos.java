package br.com.fundamentos;

public class Aula08_Booleanos {

	public static void main(String[] args) {
		// Valores Booleanos

        /*Um tipo booleano Ã© declarado com a booleanpalavra 
         * - chave e pode receber apenas os valores trueou false:*/
		
		boolean valorTrue = true;
		boolean valorFalse = false;
		
		System.out.println("\n Valor Verdadeiro " + valorTrue);
		System.out.println("\n Valor False " + valorFalse);
		
		// Exemplos
		int x = 10;
		int y = 20;
		
		System.out.println("\n X Maior que Y: ");
		System.out.println(x > y);
		
		System.out.println("\n X Menor que Y: ");
		System.out.println(x < y);
		
		
	}

}
