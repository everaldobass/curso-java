package br.com.fundamentos;

public class Aula05_OperadoresAritmeticos {
	
	public static void main(String[] args) {
		
		// Operadores aritméticos
		int num1 = 10;
		int num2 = 2;
		
	    // Adição
		int soma = num1 + num2;
		System.out.println("\n Soma: " + soma);
		
		// Subtração
		int subtracao = num1 - num2;
		System.out.println("\n Subtração: " + subtracao);
		
		// Multiplicação
		int multiplicacao = num1 * num2;
		System.out.println("\n Multiplicação: " + multiplicacao);
		
		// Divisão
		int divisao = num1 / num2;
		System.out.println("\n Divisão: " + divisao);
		
		// Modulos
		int modulos = num1 % num2;
		System.out.println("\n Modulos: " + modulos);
		
		// Incremento
		int incremento = ++num1;
		System.out.println("\n Incremento: " + incremento);
		
		// Decremento
		int decremento = --num2;
		System.out.println("\n Decremento: " + decremento);
		
		
		
	}

}
