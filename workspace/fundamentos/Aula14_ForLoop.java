package br.com.fundamentos;

public class Aula14_ForLoop {

	public static void main(String[] args) {
		// For Loop - Quando você souber exatamente quantas vezes deseja percorrer um bloco de código, use o forloop em vez de um whileloop:
		
		for (int i = 1; i <= 5; i++) {
			System.out.println("Valor de I: " + i);
		}

	}

}
